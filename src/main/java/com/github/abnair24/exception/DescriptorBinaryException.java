package com.github.abnair24.exception;

public class DescriptorBinaryException extends Exception {

    public DescriptorBinaryException() {
        super();
    }

    public DescriptorBinaryException(String message) {
        super(message);
    }

    public DescriptorBinaryException(String message, Throwable cause) {
        super(message, cause);
    }

    public DescriptorBinaryException(Throwable cause) {
        super(cause);
    }

}
